#!/usr/bin/env python3
"""
    - This module concatenates two strings.
    - Returns concatenated string
    - annotaions: str1 and str2 are strings
"""


def concat(str1: str, str2: str) -> str:
    """
        concat:
            concatincates two strings

        Args:
            str1 (str): first string placeholder
            str2 (str): second string placeholder

        Returns:
            str: returns concatenated string
    """

    return str1 + str2
