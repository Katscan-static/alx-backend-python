#!/usr/bin/env python3
"""
    - This module converts a float number (n) into a string
    - Returns a string type value of n
"""


def to_str(n: float) -> str:
    """
        to_str:
            converts value (n) float to string type

        Args:
            n (float): palceholder for a float type number

        Returns:
            str: string type representation of (n)
    """

    return str(n)
