#!/usr/bin/env python3


"""
    a type-annotated function takes a and b as float and returns a sum float
"""


def add(a: float, b: float) -> float:
    """
        add:
            function adds two float numbers

        Args:
            a (float): a is a placeholder
            b (float): b is a placeholder

        Returns:
            float: sum of a and b
    """
    return a + b
